
package negocio;


public class Reproceso {
    private int id;
    private int id_loteant;
    private int id_lotenuevo;
    private String fecha;
    private String observaciones;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_loteant() {
        return id_loteant;
    }

    public void setId_loteant(int id_loteant) {
        this.id_loteant = id_loteant;
    }

    public int getId_lotenuevo() {
        return id_lotenuevo;
    }

    public void setId_lotenuevo(int id_lotenuevo) {
        this.id_lotenuevo = id_lotenuevo;
    }

    public String getId_fecha() {
        return fecha;
    }

    public void setId_fecha(String fecha) {
        this.fecha = fecha;
    }

    public String getObservaciones() {
        return observaciones;
    }

    public void setObservaciones(String observaciones) {
        this.observaciones = observaciones;
    }
    
    
}
